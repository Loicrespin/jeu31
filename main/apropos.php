<h2>Règles du jeu</h2>

<h3>Nombre de joueurs :</h3>
<p>Deux joueurs</p>

<h3>Matériel :</h3>
<p>Un jeu de cinquante-deux cartes</p>

<h3>Objectif :</h3>
<p>Obtenir 31 ou le pointage qui s'en approche le plus avant d'épuiser la dernière carte du talon.</p>

<h3>Hiérarchie des cartes :</h3>
<p>Les As valent 11 points ; les Rois, Dames, Valets et 10  valent 10 points ; les autres cartes valent la valeur affichée sur la carte.</p>

<h3>Règle et déroulement :</h3>
<p>Le donneur mélange les cartes et distribue trois cartes à chaque joueur, une à une. Ensuite il pose le talon au milieu de la table et retourne la carte du dessus.<br><br>

Le joueur à gauche du donneur commence le jeu : soit il prend la carte retournée pour remplacer l'une des cartes de son jeu, soit il peut prélever une carte sur le talon pour remplacer l'une des cartes de son jeu. Il repose ensuite la carte en trop sur la carte ouverte. Les joueurs suivants procèdent de la même façon jusqu'à ce qu'un joueur obtienne 31, que l'on épuise la dernière carte du talon ou encore qu'un joueur décide que son pointage est suffisant pour annoncer la fin de la manche. Celui qui obtient 31 ou qui souhaite terminer la manche doit toquer sur la table pour annoncer le dernier tour de jeu. Si au cours de ce tour final un joueur obtient 31, c'est ce dernier qui remporte le point, sinon le point revient à celui qui a toqué, à condition bien sûr que son pointage soit le plus élevé. En l'absence d'un total de 31 lorsqu'il n'y a plus de cartes dans le talon, c'est le joueur qui a le pointage le plus près de 31 qui gagne le point.</p>

